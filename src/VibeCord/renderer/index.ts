import { onceReady } from "@VibeCord/types/webpack";
import { VoiceActions } from "@VibeCord/types/webpack/common";
import { React } from "@VibeCord/types/webpack/common";

import { SecurityModal } from "./components/SecurityModal";
import SettingsUi from "./components/settings/Settings";
import { VesktopLogger } from "./logger";
import { Settings } from "./settings";
export { Settings };

import type SettingsPlugin from "@VibeCord/types/plugins/_core/settings";
import { VesktopSettingsIcon } from "shared/icons";

VesktopLogger.log("read if cute :3");
VesktopLogger.log(`VibeCord v${VesktopNative.app.getVersion()}`);

const { customEntries, customSections } = Vencord.Plugins.plugins.Settings as any as typeof SettingsPlugin;

customEntries.push({
    key: "VibeCord_VibeCord_settings",
    title: "VibeCord Settings",
    Component: SettingsUi,
    Icon: VesktopSettingsIcon
});

customSections.push(() => ({
    section: "VibeCordSettings",
    label: "VibeCord Settings",
    element: SettingsUi,
    className: "vc-VibeCord-settings"
}));

VesktopNative.voice.onToggleSelfMute(() => VoiceActions.toggleSelfMute());
VesktopNative.voice.onToggleSelfDeaf(() => VoiceActions.toggleSelfDeaf());

// Afficher le SecurityModal uniquement au premier lancement
const SECURITY_MODAL_KEY = "vibecord_security_modal_seen";
onceReady.then(() => {
    if (!localStorage.getItem(SECURITY_MODAL_KEY)) {
        setTimeout(() => {
            openModal(props => React.createElement(SecurityModal, props));
            localStorage.setItem(SECURITY_MODAL_KEY, "1");
        }, 1500);
    }
});

// TODO: remove this legacy workaround once some time has passed
if (!Vencord.Api.Styles.vencordRootNode) {
    const style = document.createElement("style");
    style.id = "vesktop-css-core";
    VesktopNative.app.getRendererCss().then(css => (style.textContent = css));
    document.addEventListener("DOMContentLoaded", () => document.documentElement.append(style), { once: true });
}
